function adicao(numero1, numero2) {

    let resultadoDaAdicao = numero1 + numero2

    return console.log('A soma dos dois números é: ' + resultadoDaAdicao)
}


function divisao(numero1, numero2) {
    let resultadoDaDivisao = numero1 / numero2
    
    return console.log('O resultado da divisão dos dois números é: ' + resultadoDaDivisao)
}


function multiplicacao(numero1, numero2) {
    let resultadoDaMultiplicacao = numero1 * numero2
    
    return console.log("O resultado da multiplicação dos dois números é " + resultadoDaMultiplicacao)
    
}


function subtracao(numero1, numero2) {
    let resultadoDaSubtracao = numero1 - numero2
    
    return console.log("O resultado da subtração entre os dois números é " + resultadoDaSubtracao)
}

